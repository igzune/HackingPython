def multiplicacion(v1, v2):
    r = v1 * v2
    print('El resultado es', r, '\n')

def divicion(v1, v2):
    r = v1 / v2
    print('El resultado es', r, '\n')

def suma(v1, v2):
    r = v1 + v2
    print('El resultado es', r, '\n')

def resta(v1, v2):
    print('La resultado es: {}'.format(v1-v2), '\n')
